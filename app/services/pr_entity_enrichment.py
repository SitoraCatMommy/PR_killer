"""Lightweight PR framing for extracted units (no extra LLM call)."""

from __future__ import annotations

from app.domain.enums import EntityType
from app.models.extracted_entity import ExtractedEntity


def pr_fields_for_entity(entity: ExtractedEntity) -> tuple[str, str]:
    """Return (pr_implication, messaging_guidance) in Russian for strategist UI."""
    title = (entity.title or "").strip()[:200]
    et = entity.entity_type

    if et == EntityType.RISK:
        impl = (
            f"Сигнал «{title}» повышает чувствительность аудитории к негативному сценарию вокруг бренда и может усилить "
            "скепсис в СМИ и соцсетях, если тему не закрыть коммуникационно."
        )
        guide = (
            "Зафиксировать фактический контур риска, подготовить Q&A и держателя линии; избегать размытых обещаний — "
            "опереться на проверяемые формулировки и единый тон во всех каналах."
        )
    elif et == EntityType.TRUST_ISSUE:
        impl = (
            f"Тема «{title}» бьёт по доверию: при слабом ответе растёт разрыв между ожиданиями и нарративом бренда."
        )
        guide = (
            "Сместить акцент на прозрачность процессов и контроль для пользователя; дать спикеру короткие тезисы "
            "безопасности и сценарии ответа на провокационные вопросы."
        )
    elif et == EntityType.HYPOTHESIS:
        impl = (
            f"Гипотеза «{title}» задаёт направление, в котором может развернуться медиаполе — полезно для опережающей PR-подготовки."
        )
        guide = (
            "Проверить обсуждение темы в доверенных каналах; при подтверждении сигнала — подготовить позицию и "
            "инфоповоды, не смешивая гипотезу с подтверждённым фактом."
        )
    elif et == EntityType.BEHAVIOR_PATTERN:
        impl = (
            f"Паттерн «{title}» показывает, как аудитория реально потребляет информацию — это определяет, где бренд "
            "выигрывает или проигрывает внимание."
        )
        guide = (
            "Согласовать приоритет каналов и форматов коммуникации с этим паттерном; адаптировать месседжи под носители, "
            "которым доверяют в интервью."
        )
    elif et in (EntityType.PROBLEM, EntityType.PAIN_POINT, EntityType.USER_NEED):
        impl = (
            f"Формулировка «{title}» отражает ожидания и боли аудитории — напрямую влияет на готовность доверять "
            "финтех-коммуникациям и воспринимать официальные сообщения."
        )
        guide = (
            "Перевести боль в язык выгоды и контроля без продуктового «фиксим фичей»; подготовить короткие ответы "
            "для спикера и соцсетей в тоне спокойной уверенности."
        )
    elif et == EntityType.ADOPTION_BARRIER:
        impl = f"Барьер «{title}» снижает готовность к действию — риск для репутации «сложного» или «непрозрачного» сервиса."
        guide = (
            "Разложить барьер на коммуникационные узлы: что объяснить первым, где дать подтверждение доверия; "
            "не спорить с эмоцией — снимать её фактурой и сценариями."
        )
    elif et in (EntityType.OPEN_QUESTION, EntityType.SENTIMENT_SIGNAL):
        impl = (
            f"Сигнал «{title}» задаёт зону неопределённости в голове аудитории — там легко появляются слухи и искажения."
        )
        guide = (
            "Зафиксировать вопрос в Q&A; при появлении в медиаполе — быстрый фактический ответ и единая линия для команды."
        )
    else:
        impl = (
            f"Единица «{title}» влияет на то, как аудитория читает бренд в повестке; стоит явно привязать её к линии "
            "коммуникаций, а не оставлять только в исследовательских заметках."
        )
        guide = (
            "Сформулировать один ясный месседж для внешних каналов и проверить, не противоречит ли он другим "
            "опорным тезисам бренда."
        )

    return impl, guide
